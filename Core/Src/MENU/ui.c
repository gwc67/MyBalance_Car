void cartoon_move_ui(UI_* ui, uint8_t spd)
{
    if (ui->base.x == ui->base.tarX && ui->base.y == ui->base.tarY)
    {
        return;
    }
    int8_t stepx;
    int8_t stepy;
    int8_t th = spd;
    if (abs(ui->base.tarX -  ui->base.x) > th)
    {
        stepx = (ui->base.tarX - ui->base.x) / th;
    }
    else
    {
        stepx = ( ui->base.tarX -  ui->base.x> 0)? 1 : (ui->base.tarX -  ui->base.x < 0)? -1 : 0;
    }
    if (abs(ui->base.tarY - ui->base.y )> th)
    {
        stepy = (ui->base.tarY - ui->base.y) / th;
    }
    else
    {
        stepy = (ui->base.tarY - ui->base.y > 0)? 1 : (ui->base.tarY - ui->base.y < 0)? -1 : 0;
    }
    
    ui->base.x += stepx;
    ui->base.y += stepy;

}

void cartoon_zoom_ui(UI_* ui, uint8_t spd)
{
    if (ui->state != UI_BASE_IMG)
    {
        return;
    }
    if (ui->IMG.high == ui->IMG.tarHigh && ui->IMG.wide == ui->IMG.tarWide)
    {
        return;
    }

    int8_t stepx;
    int8_t stepy;
    int8_t th = spd;

    if (abs(ui->IMG.tarWide -  ui->IMG.wide) > th)
    {
        stepx = (ui->IMG.tarWide -  ui->IMG.wide) / th;
    }
    else{
        stepx = (ui->IMG.tarWide -  ui->IMG.wide > 0)? 1: (ui->IMG.tarWide - ui->IMG.wide < 0)? -1 : 0;
    }
    if (abs(ui->IMG.tarHigh - ui->IMG.high) > th)
    {
        stepy = (ui->IMG.tarHigh - ui->IMG.high ) / th;
    }
    else{
        stepy = (ui->IMG.tarHigh - ui->IMG.high > 0)? 1: (ui->IMG.tarHigh - ui->IMG.high < 0)? -1 : 0;
    }
    
    ui->IMG.wide += stepx;
    ui->IMG.high += stepy;
}

